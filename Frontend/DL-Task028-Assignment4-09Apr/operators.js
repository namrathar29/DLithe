let x = 10;
let y = 5;

// addition
document.write('x + y = ', x + y);
document.write("<br>")

// subtraction
document.write('x - y = ', x - y);
document.write("<br>")

// multiplication
document.write('x * y = ', x * y);
document.write("<br>")

// division
document.write('x / y = ', x / y);
document.write("<br>")

// remainder
document.write('x % y = ', x % y);
document.write("<br>")

// increment
document.write('++x = ', ++x);
document.write("<br>")
document.write('x++ = ', x++);
document.write("<br>")
document.write('x = ', x); 
document.write("<br>")

// decrement
document.write('--x = ', --x); 
document.write("<br>")
document.write('x-- = ', x--);
document.write("<br>") 
document.write('x = ', x);
document.write("<br>")

//exponentiation
document.write('x ** y =', x ** y);
document.write("<br>")